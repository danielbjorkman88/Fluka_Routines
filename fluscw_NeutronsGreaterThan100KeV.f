        subroutine locate(xx,n,x,j)
        integer j,n
        double precision x, xx(n)
        integer jl, jm, ju
        jl = 0
        ju = n+1
  10    if (ju-jl.gt.1) then
          jm = (ju+jl)/2
          if ((xx(n).ge.xx(1)).eqv.(x.ge.xx(jm)))then
           jl = jm
          else
           ju = jm
        endif
        goto 10
        endif
        if(x.eq.xx(1))then
          j = 1
        else if(x.eq.xx(n))then
          j = n-1
        else
          j = jl
        endif
        return
        end 

        double precision function ygetValue(ENE, Nbins, XS, energy)
        integer Nbins, ind
        double precision ENE(Nbins+1), XS(Nbins), energy

        call locate(ENE, Nbins+1, energy, ind)
        if (ind .EQ. 0) then
          ygetValue = 0.D0
        else if (ind .GE. Nbins+1) then
          ygetValue = XS(Nbins)
        else
          ygetValue = XS(ind)
        endif
        
        return
        end 

